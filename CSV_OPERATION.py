#CSVファイル-------------------------------------------------------------#
with open(FILE_1) as f:
    reader = csv.reader(f)
    l = [row for row in reader]
print(l)
# [['11', '12', '13', '14'], ['21', '22', '23', '24'], ['31', '32', '33', '34']]
#行の取得（2列目）-------------------------------------------------------#
print(l[1])
